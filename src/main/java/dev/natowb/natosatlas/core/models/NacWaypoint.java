package dev.natowb.natosatlas.core.models;

public class NacWaypoint {

    public final String name;
    public final int x;
    public final int y;
    public final int z;

    public NacWaypoint(String name, int x, int y, int z) {
        this.name = name;
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
