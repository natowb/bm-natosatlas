package dev.natowb.natosatlas.core.waypoint;

public class Waypoint {

    public final String name;
    public final int x;
    public final int y;
    public final int z;

    public Waypoint(String name, int x, int y, int z) {
        this.name = name;
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
